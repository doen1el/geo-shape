/**
 * Generates the third-party open-source license list shown on /licenses.
 *
 * Run:  node scripts/build-licenses.mjs
 */
import { readFileSync, writeFileSync, readdirSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const pkg = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8'));

const names = [...new Set([
	...Object.keys(pkg.dependencies ?? {}),
	...Object.keys(pkg.devDependencies ?? {})
])].sort((a, b) => a.toLowerCase().localeCompare(b.toLowerCase()));

function readLicenseText(dir) {
	let files;
	try {
		files = readdirSync(dir);
	} catch {
		return '';
	}
	const hit = files.find((f) => /^(LICENSE|LICENCE|COPYING)(\.\w+)?$/i.test(f));
	if (!hit) return '';
	try {
		return readFileSync(join(dir, hit), 'utf8').trim();
	} catch {
		return '';
	}
}

function urlFor(p, name) {
	if (typeof p.homepage === 'string' && p.homepage) return p.homepage;
	const repo = p.repository;
	let u = typeof repo === 'string' ? repo : repo?.url;
	if (u) {
		u = u
			.replace(/^git\+/, '')
			.replace(/^git:\/\//, 'https://')
			.replace(/^ssh:\/\/git@/, 'https://')
			.replace(/\.git$/, '');
		if (u.startsWith('github:')) u = 'https://github.com/' + u.slice(7);
		return u;
	}
	return `https://www.npmjs.com/package/${name}`;
}

function licenseId(p) {
	if (typeof p.license === 'string') return p.license;
	if (p.license?.type) return p.license.type;
	if (Array.isArray(p.licenses)) return p.licenses.map((l) => l.type).join(' / ');
	return 'See license';
}

const entries = [];
for (const name of names) {
	const dir = join(root, 'node_modules', name);
	const pjPath = join(dir, 'package.json');
	if (!existsSync(pjPath)) {
		console.warn(`skip (not installed): ${name}`);
		continue;
	}
	const p = JSON.parse(readFileSync(pjPath, 'utf8'));
	entries.push({
		name,
		version: p.version ?? '',
		license: name === '@dicebear/styles' ? 'Various (per avatar style)' : licenseId(p),
		description: p.description ?? '',
		url: urlFor(p, name),
		text: readLicenseText(dir)
	});
}

const extras = [
	{
		name: 'Natural Earth',
		version: '',
		license: 'Public Domain',
		description: 'Public-domain map dataset used to generate the country and region outlines.',
		url: 'https://www.naturalearthdata.com/about/terms-of-use/',
		text:
			'Natural Earth vector and raster map data is in the public domain. ' +
			'You may use the maps in any manner, including modifying the content and design, ' +
			'electronic dissemination, and offset printing. No permission is needed.\n\n' +
			'See https://www.naturalearthdata.com/about/terms-of-use/'
	}
];

const all = [...entries, ...extras];

const banner =
	'// AUTO-GENERATED by scripts/build-licenses.mjs — do not edit by hand.\n' +
	`// Re-run: node scripts/build-licenses.mjs (${all.length} entries)\n\n`;

const body =
	'export interface LicenseEntry {\n' +
	'\tname: string;\n' +
	'\tversion: string;\n' +
	'\tlicense: string;\n' +
	'\tdescription: string;\n' +
	'\turl: string;\n' +
	'\ttext: string;\n' +
	'}\n\n' +
	'export const LICENSES: LicenseEntry[] = ' +
	JSON.stringify(all, null, '\t') +
	';\n';

const outPath = join(root, 'src', 'lib', 'data', 'licenses.ts');
writeFileSync(outPath, banner + body);
console.log(`Wrote ${all.length} license entries → src/lib/data/licenses.ts`);
